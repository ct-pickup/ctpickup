create or replace function public.chat_message_body_matches_profanity(p_body text)
returns boolean
language sql
stable
set search_path = public
as $$
  select exists (
    select 1
    from public.chat_profanity_terms t
    cross join lateral (
      select
        trim(both ' ' from regexp_replace(lower(coalesce(p_body, '')), '[^a-z0-9]+', ' ', 'g')) as collapsed,
        (' ' || trim(both ' ' from regexp_replace(lower(coalesce(p_body, '')), '[^a-z0-9]+', ' ', 'g')) || ' ') as padded
    ) m
    where
      (
        trim(both ' ' from regexp_replace(lower(t.term), '[^a-z0-9]+', ' ', 'g')) like '% %'
        and strpos(
          m.padded,
          ' ' || trim(both ' ' from regexp_replace(lower(t.term), '[^a-z0-9]+', ' ', 'g')) || ' '
        ) > 0
      )
      or
      (
        trim(both ' ' from regexp_replace(lower(t.term), '[^a-z0-9]+', ' ', 'g')) not like '% %'
        and exists (
          select 1
          from unnest(string_to_array(m.collapsed, ' ')) as x(word)
          where word = trim(both ' ' from regexp_replace(lower(t.term), '[^a-z0-9]+', ' ', 'g'))
            and length(word) > 0
        )
      )
  );
$$;

create or replace function public.chat_messages_enforce_no_profanity()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if public.chat_message_body_matches_profanity(new.body) then
    raise exception 'Profanity is not allowed in chat.'
      using errcode = '23514';
  end if;
  return new;
end;
$$;

drop trigger if exists chat_messages_profanity_guard_trg on public.chat_messages;
create trigger chat_messages_profanity_guard_trg
  before insert or update of body on public.chat_messages
  for each row
  execute function public.chat_messages_enforce_no_profanity();


-- Only admins may create new chat rooms (team / private). Team room is seeded by migrations.
drop policy if exists "chat_rooms_insert_admin_only" on public.chat_rooms;
create policy "chat_rooms_insert_admin_only"
  on public.chat_rooms
  for insert
  to authenticated
  with check (
    exists (
      select 1 from public.profiles p
      where p.id = auth.uid() and p.is_admin = true
    )
  );
